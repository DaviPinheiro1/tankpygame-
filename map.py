class Map:
    def __init__(self, data):
        self.tile_list = []
        dirt = pygame.image.load("assets/black_dirt.png")
        row_count = 0
        for row in data:
            col_count = 0
            for block in row:
                if block == 1:
                    img = pygame.transform.scale(dirt, (30, 30))
                    img_rect = img.get_rect()
                    img_rect.x = col_count * 30
                    img_rect.y = row_count * 30
                    tile = (img, img_rect)
                    self.tile_list.append(tile)
                col_count += 1
            row_count += 1

    def draw(self, screen):
        for tile in self.tile_list:
            screen.blit(tile[0], tile[1])

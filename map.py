#leitura do mapa
class World():
    def __init__(self, data):
        self.tile_list = []
        dirt = pygame.image.load("dirt.png")
        fileira_count = 0
        for fileira in data:
            coluna_count = 0
            for bloco in fileira:
                if bloco == 1:
                    img = pygame.transform.scale(dirt,(30, 30))
                    img_rect = img.get_rect()
                    img_rect.x = coluna_count * 30
                    img_rect.y = fileira_count * 30
                    tile = (img, img_rect )
                    self.tile_list.append(tile)
                coluna_count += 1
            fileira_count += 1
    
    def draw(self, tela):
        for tile in self.tile_list:
            tela.blit(tile[0], tile[1])
            # hitbox das terras
            pygame.draw.rect(tela, (255,255,255), tile[1], 1)

    
